public class Point3D {
    private int x3D=0;
    private int y3D=0;
    private int z3D=0;

    public Point3D() {
    }

    public Point3D(int x3D, int y3D, int z3D) {
        this.x3D = x3D;
        this.y3D = y3D;
        this.z3D = z3D;
    }

    public int getX3D() {
        return x3D;
    }

    public void setX3D(int x3D) {
        this.x3D = x3D;
    }

    public int getY3D() {
        return y3D;
    }

    public void setY3D(int y3D) {
        this.y3D = y3D;
    }

    public int getZ3D() {
        return z3D;
    }

    public void setZ3D(int z3D) {
        this.z3D = z3D;
    }
}
