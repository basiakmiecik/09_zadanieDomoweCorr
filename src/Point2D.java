public class Point2D {
    private int x2D;
    private int y2D;

    public Point2D() {
    }

    public Point2D(int x2D, int y2D) {
        this.x2D = x2D;
        this.y2D = y2D;
    }

    public int getX2D() {
        return x2D;
    }

    public void setX2D(int x2D) {
        this.x2D = x2D;
    }

    public int getY2D() {
        return y2D;
    }

    public void setY2D(int y2D) {
        this.y2D = y2D;
    }
}
